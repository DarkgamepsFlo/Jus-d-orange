    <p class="paragraph"> Naissance le 12 mars 1816 à Gravelines<br>
        Fils de Jacques François (marin pêcheur) (1791 -…) et de Marie Louise Victoire BOUCLET<br>
        Décès en 17 avril 1901<br></p>

        <p class="paragraph">
            En quarante-trois, âgé de  27 ans, le matelot du pilotage Ficquet ne savait ni lire ni écrire.<br>
    
            Malgré sa parfaite connaissance des côtes, il n’aurait pu, faute des premiers éléments d’instruction, se présenter aux examens pour l’emploi d’aspirant pilote.<br>
    
            Toute sa carrière, il serait resté simple matelot avec une paye insuffisante pour nourrir sa femme, qui lui avait déjà donné cinq enfants.<br>
    
            Il se fit inscrire pour suivre un cours d’adultes, ouvert depuis plusieurs années à Dunkerque. En revenant de son service de mer, il allait résolument, tous les soirs, à l’école, et, grâce à son énergie, il put vite acquérir les connaissances indispensables pour obtenir la position de pilote, but honorable de sa légitime ambition.<br>
